
# This will tab \t

tabby_cat = "\tI'm tabbed in."

# \n drops down a line

persian_cat = "I'm split\non a line."

# If we want to use a '\', we use '\\'
backslash_cat = "I'm \\ a \\ cat."

# If we want to type a big paragraph, use 3 quotes """ and put your text
# inside the the triple quotes like this
# """
fat_cat = """
I'll do a list:
\t* Cat food
\t* Fishies
\t* Catnip\n\t* Grass
"""

print(tabby_cat)
print(persian_cat)
print(backslash_cat)
print(fat_cat)
